public class BuscarEstudiante implements Controller {

  private MiFacade mi;
  public void setMi(MiFacade mi) { this.mi = mi; }

  public ModelAndView handleRequest(HttpServletRequest request, HttpServletResponse response) throws Exception {
    Map modelo = new HashMap();
     
    Clientes cliente = (Clientes) request.getSession().getAttribute("__sess_cliente");
    if(cliente==null) return new ModelAndView("Aviso", "mensaje", "Su sesion ha terminado. Vuelva a la pagina inicial e ingrese de nuevo.");
    
    modelo.put("gestion",request.getParameter("gestion"));    
    modelo.put("periodo",request.getParameter("periodo"));
    
    return new ModelAndView("memorandumsAuxiliar/buscarEstudiante/EntradaBusqueda",modelo);
  }
}
